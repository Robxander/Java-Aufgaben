public class Wiki2 {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;

        System.out.println("(" + a + ") + (" + b + ") = " + (a + b));
        System.out.println("(" + a + ") - (" + b + ") = " + (a - b));
        System.out.println("(" + a + ") * (" + b + ") = " + (a * b));
        System.out.println("(" + a + ") : (" + b + ") = " + ((double)a / (double)b));
    }
}
